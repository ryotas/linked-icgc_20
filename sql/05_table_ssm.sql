DROP TABLE IF EXISTS ssm;
CREATE TABLE IF NOT EXISTS ssm (
  `icgc_mutation_id` varchar(255) DEFAULT NULL,
  `icgc_donor_id` varchar(255) DEFAULT NULL,
  `project_code` varchar(255) DEFAULT NULL,
  `icgc_specimen_id` varchar(255) DEFAULT NULL,
  `icgc_sample_id` varchar(255) DEFAULT NULL,
  `matched_icgc_sample_id` varchar(255) DEFAULT NULL,
  `submitted_sample_id` varchar(255) DEFAULT NULL,
  `submitted_matched_sample_id` varchar(255) DEFAULT NULL,
  `chromosome` varchar(255) DEFAULT NULL,
  `chromosome_start` int(10) DEFAULT NULL,
  `chromosome_end` int(10) DEFAULT NULL,
  `chromosome_strand` int(10) DEFAULT NULL,
  `assembly_version` varchar(255) DEFAULT NULL,
  `mutation_type` varchar(255) DEFAULT NULL,
  `reference_genome_allele` varchar(255) DEFAULT NULL,
  `mutated_from_allele` varchar(255) DEFAULT NULL,
  `mutated_to_allele` varchar(255) DEFAULT NULL,
  `quality_score` varchar(255) DEFAULT NULL,
  `probability` decimal(7,6) DEFAULT NULL,
  `total_read_count` int(10) DEFAULT NULL,
  `mutant_allele_read_count` int(10) DEFAULT NULL,
  `verification_status` varchar(255) DEFAULT NULL,
  `verification_platform` varchar(255) DEFAULT NULL,
