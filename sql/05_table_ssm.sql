  `cds_mutation` varchar(255) DEFAULT NULL,
  `gene_affected` varchar(255) DEFAULT NULL,
  `transcript_affected` varchar(225) DEFAULT NULL,
  `gene_build_version` int(10) DEFAULT NULL,
  `platform` varchar(255) DEFAULT NULL,
  `experimental_protocol` varchar(255) DEFAULT NULL,
  `sequencing_strategy` varchar(255) DEFAULT NULL,
  `base_calling_algorithm` varchar(255) DEFAULT NULL,
  `alignment_algorithm` varchar(225) DEFAULT NULL,
  `variation_calling_algorithm` varchar(255) DEFAULT NULL,
  `other_analysis_algorithm` varchar(255) DEFAULT NULL,
  `seq_coverage` varchar(255) DEFAULT NULL,
  `raw_data_repository` varchar(255) DEFAULT NULL,
  `raw_data_accession` varchar(255) DEFAULT NULL,
  `initial_data_release_date` varchar(255) DEFAULT NULL
);
